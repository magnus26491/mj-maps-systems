// ─── VAN LOADING MANIFEST ────────────────────────────────────────────────────

export interface VanLoadingManifest {
  routeId: string;
  vehicleId: string;
  generatedAt: string;
  /** Stops sorted in LOADING order (last stop = first loaded = front of van) */
  loadingOrder: Array<{
    stopId: string;
    sequenceNumber: number;
    addressLine1: string;
    postcode: string;
    parcel?: ParcelSpec;
    loadPosition: VanLoadPosition;
    trolleyLevel?: TrolleyAdvisoryLevel;
  }>;
  totalStops: number;
  totalParcels: number;
  totalWeightKg: number;
}

/**
 * Build a van loading manifest from an optimised route.
 * Stops are reversed (last delivery = load first at front of van).
 * Bays are assigned: first 33% of deliveries = rear, middle 33% = mid, last 33% = front.
 */
export function buildVanLoadingManifest(
  routeId: string,
  vehicleId: string,
  stops: StopPoint[],
): VanLoadingManifest {
  const ordered = [...stops].sort((a, b) => a.sequenceNumber - b.sequenceNumber);
  const total   = ordered.length;

  const loadingOrder = ordered.map((stop, i) => {
    const deliveryOrder = i + 1;
    // Loading order is reverse of delivery order
    const loadOrder = total - i;

    // Assign bay based on thirds
    let bay: 'FRONT' | 'MID' | 'REAR';
    if (loadOrder <= Math.ceil(total / 3))      bay = 'REAR';  // loaded last, delivered last
    else if (loadOrder <= Math.ceil(2 * total / 3)) bay = 'MID';
    else                                            bay = 'FRONT'; // loaded first, delivered first

    const side: 'LEFT' | 'RIGHT' = loadOrder % 2 === 0 ? 'LEFT' : 'RIGHT';
    const shelf: 'TOP' | 'BOTTOM' = (stop.parcel?.totalWeightKg ?? 0) > 15 ? 'BOTTOM' : 'TOP';

    const loadPosition: VanLoadPosition = {
      bay,
      shelf,
      side,
      label: `${bay.charAt(0) + bay.slice(1).toLowerCase()} ${side.toLowerCase()}, ${shelf.toLowerCase()} shelf`,
      deliveryOrder,
    };

    return {
      stopId:         stop.id,
      sequenceNumber: stop.sequenceNumber,
      addressLine1:   stop.addressLine1,
      postcode:       stop.postcode,
      parcel:         stop.parcel,
      loadPosition,
      trolleyLevel:   stop.trolleyLevel,
    };
  });

  const totalParcels   = ordered.reduce((sum, s) => sum + (s.parcel?.count ?? 1), 0);
  const totalWeightKg  = ordered.reduce((sum, s) => sum + (s.parcel?.totalWeightKg ?? 0), 0);

  return {
    routeId,
    vehicleId,
    generatedAt: new Date().toISOString(),
    loadingOrder: loadingOrder.reverse(), // Reverse: first in list = first to load into van
    totalStops: total,
    totalParcels,
    totalWeightKg: Math.round(totalWeightKg * 10) / 10,
  };
}
